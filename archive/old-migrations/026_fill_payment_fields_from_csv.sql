-- Update deals matched by normalized phone
UPDATE hubspot_deals_raw d
SET
  number_of_installments__months = csv.installments,
  payment_status = csv.payment_status,
  updated_at = NOW()
FROM csv_payment_fields csv
INNER JOIN hubspot_contacts_raw c ON
  REGEXP_REPLACE(c.phone, '[^0-9]', '', 'g') = csv.phone_normalized
WHERE
  d.raw_json->'associations'->'contacts'->'results'->0->>'id' = c.hubspot_id
  AND d.dealstage = 'closedwon';

