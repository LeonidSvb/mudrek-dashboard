SET
  closedate = csv.closedate_new,
  updated_at = NOW()
FROM csv_closedate_updates_v2 csv
INNER JOIN hubspot_contacts_raw c ON
  REGEXP_REPLACE(c.phone, '[^0-9]', '', 'g') = csv.phone_normalized
WHERE
  d.raw_json->'associations'->'contacts'->'results'->0->>'id' = c.hubspot_id
  AND d.dealstage = 'closedwon'
  AND csv.closedate_new IS NOT NULL;

